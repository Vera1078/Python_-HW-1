# Напишите программу, которая принимает на вход цифру, обозначающую день недели, и проверяет, является ли этот день выходным.

# # Пример:

# - 6 -> да
#  - 7 -> да
#  - 1 -> нет

a = int(input('Введите положительное число от 1 до 7: '))

if a == 6 or a == 7:
    print('выходной день')

elif a <= 0 or a > 7:
    print('Число введено неверно!')    

else:
    print('будний день')   

